package com.example.rodrigosantos.androidstudioprojects.weatherforecastbycityccp3anbua;

class WeatherDataDetails {
    private double tem_min;
    private double temp_max;
    private  int humidity;


    public double getTem_min() {
        return tem_min;
    }

    public void setTem_min(double tem_min) {
        this.tem_min = tem_min;
    }

    public double getTemp_max() {
        return temp_max;
    }

    public void setTemp_max(double temp_max) {
        this.temp_max = temp_max;
    }

    public int getHumidity() {
        return humidity;
    }

    public void setHumidity(int humidity) {
        this.humidity = humidity;
    }
}
